static inline uint32_t decode(const char *utf8_str, int *counted, register int stop_at_invalid)
{
  register uint32_t state = UTF8_ACCEPT;
  register uint32_t codep = 0;

  register const unsigned char *s = (const unsigned char *) utf8_str;
  register int  count = 0;

  for(; *s; ++s) {
    register uint32_t byte = *s;
    register uint32_t type = utf8d[byte];

    codep = (state != UTF8_ACCEPT) ?
      (byte & 0x3fu) | (codep << 6) :
      (0xff >> type) & (byte);

    state = utf8d[256 + (state * 16) + type];

    if (!state) { count += 1; }
    else if (stop_at_invalid && state == UTF8_REJECT) { break; }
  }

  *counted = count;

  return (state == UTF8_ACCEPT) ? UTF8_ACCEPT : UTF8_REJECT;
}


int is_utf8_c(const char *s)
{
  int c = 0;
  if (decode(s, &c, TRUE) == UTF8_ACCEPT) {
    return TRUE;
  } else {
    return FALSE;
  }
}
